let messagesZhHant: Record<MessageKey, string> | null = null

function getZhHant(): Record<MessageKey, string> {
  if (!messagesZhHant) {
    messagesZhHant = Object.fromEntries(
      Object.entries(messagesZhHans).map(([k, v]) => [k, convertToTraditional(v)]),
    ) as Record<MessageKey, string>
  }
  return messagesZhHant
}

export function getMessages(locale: Locale): Record<MessageKey, string> {
  if (locale === 'zh-Hans') return messagesZhHans
  if (locale === 'zh-Hant') return getZhHant()
  if (locale === 'en') return messagesEn
  const extra = extraLocaleBundles[locale]
  if (extra) return { ...messagesEn, ...extra }
  return messagesEn
}

export function formatMessage(
  template: string,
  vars: Record<string, string | number>,
): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? ''))
}
