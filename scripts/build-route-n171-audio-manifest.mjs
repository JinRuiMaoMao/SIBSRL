import { existsSync, readdirSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import {
  ROUTE_N171_AT_PREFIX,
  ROUTE_N171_ID,
} from './lib/route-n171-audio-map.mjs'

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
export const ROUTE_N171_AUDIO_PUBLIC = resolve(root, 'public', 'audio', 'routes', ROUTE_N171_ID)
const manifestFile = resolve(root, 'src', 'data', 'routeStopAudioN171.ts')

export function buildRouteN171AudioManifest(options = {}) {
  const audioDir = options.audioDir ?? ROUTE_N171_AUDIO_PUBLIC

  /** @type {Record<number, number[]>} */
  const indicesByGroup = { 0: [], 1: [] }

  if (existsSync(audioDir)) {
    for (const file of readdirSync(audioDir)) {
      const match = file.match(/^n171-([ns])-at-(\d+)\.mp3$/i)
      if (!match) continue
      const groupIndex = match[1].toLowerCase() === 'n' ? 0 : 1
      const at = Number.parseInt(match[2], 10)
      indicesByGroup[groupIndex].push(at)
    }
  }

  for (const key of [0, 1]) {
    indicesByGroup[key] = [...new Set(indicesByGroup[key])].sort((a, b) => a - b)
  }

  const directionLines = [0, 1]
    .map((groupIndex) => {
      const indices = indicesByGroup[groupIndex] ?? []
      const arr = indices.map((i) => String(i)).join(', ')
      return `  ${groupIndex}: [${arr}] as const,`
    })
    .join('\n')

  const contents = `/** Generated by scripts/build-route-n171-audio-manifest.mjs — do not edit by hand. */

import { routes } from './routes'
import type { RouteStopAudioAtRow } from './routeStopAudio21A'

export const ROUTE_N171_ID = ${JSON.stringify(ROUTE_N171_ID)}
export const ROUTE_N171_AUDIO_DIR = './audio/routes/${ROUTE_N171_ID}'
export const ROUTE_N171_AT_PREFIX = ${JSON.stringify(ROUTE_N171_AT_PREFIX)}

const ROUTE_N171_AT_STOP_INDICES: Readonly<Record<number, readonly number[]>> = {
${directionLines}
}

function audioUrlForAtStop(directionGroupIndex: number, atStopIndex: number): string {
  const dirKey = directionGroupIndex === 0 ? 'n' : 's'
  return \`\${ROUTE_N171_AUDIO_DIR}/\${ROUTE_N171_AT_PREFIX}-\${dirKey}-at-\${String(atStopIndex).padStart(2, '0')}.mp3\`
}

export function getRouteN171StopAudioByAtIndex(
  directionGroupIndex: number,
): Map<number, RouteStopAudioAtRow> | null {
  const route = routes.find((r) => r.id === ROUTE_N171_ID)
  const stopList = route?.stops?.[directionGroupIndex]?.list
  const indices = ROUTE_N171_AT_STOP_INDICES[directionGroupIndex]
  if (!stopList?.length || !indices?.length) return null

  const map = new Map<number, RouteStopAudioAtRow>()

  for (const at of indices) {
    const next = stopList[at + 1]
    const label = next?.name
    if (!label) continue
    map.set(at, {
      atStopIndex: at,
      nextStopLabel: label,
      audioUrl: audioUrlForAtStop(directionGroupIndex, at),
    })
  }

  return map.size > 0 ? map : null
}
`

  writeFileSync(manifestFile, contents, 'utf8')
  return {
    north: indicesByGroup[0].length,
    south: indicesByGroup[1].length,
  }
}

const isMain = process.argv[1]?.endsWith('build-route-n171-audio-manifest.mjs')
if (isMain) {
  const result = buildRouteN171AudioManifest()
  console.log(`[n171] manifest: N=${result.north}, S=${result.south} clip(s)`)
}
