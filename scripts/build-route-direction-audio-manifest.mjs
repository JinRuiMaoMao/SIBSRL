import { existsSync, readdirSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { routeAudioFilePrefix, loadRouteStopGroups } from './lib/route-direction-audio-map.mjs'

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
export const ROUTE_AUDIO_PUBLIC_ROOT = resolve(root, 'public', 'audio', 'routes')
const manifestFile = resolve(root, 'src', 'data', 'routeStopAudioByDirection.generated.ts')

/** @returns {string[]} */
export function discoverSyncedDirectionRouteIds(options = {}) {
  const audioRoot = options.audioRoot ?? ROUTE_AUDIO_PUBLIC_ROOT
  if (!existsSync(audioRoot)) return []

  const routeIds = []
  for (const routeId of readdirSync(audioRoot, { withFileTypes: true })) {
    if (!routeId.isDirectory()) continue
    if (routeId.name === 'common' || routeId.name === 'stop-names') continue
    if (scanDirectionAudioIndices(audioRoot, routeId.name).size > 0) {
      routeIds.push(routeId.name)
    }
  }
  return routeIds.sort((a, b) => a.localeCompare(b, 'en'))
}

/**
 * @param {string} audioRoot
 * @param {string} routeId
 * @returns {Map<number, number[]>}
 */
export function scanDirectionAudioIndices(audioRoot, routeId) {
  const dir = resolve(audioRoot, routeId)
  /** @type {Map<number, number[]>} */
  const indicesByGroup = new Map()
  if (!existsSync(dir)) return indicesByGroup

  const groups = loadRouteStopGroups(routeId) ?? []
  const prefix = routeAudioFilePrefix(routeId)
  const pattern = new RegExp(`^${prefix}-([nsew])-at-(\\d+)\\.mp3$`, 'i')

  for (const file of readdirSync(dir)) {
    const match = file.match(pattern)
    if (!match) continue
    const dirKey = match[1].toLowerCase()
    const at = Number.parseInt(match[2], 10)
    const groupIndex = groups.findIndex((group) => group.directionKey === dirKey)
    const resolvedGroupIndex = groupIndex >= 0 ? groupIndex : dirKey === 'n' || dirKey === 'e' ? 0 : 1
    const list = indicesByGroup.get(resolvedGroupIndex) ?? []
    list.push(at)
    indicesByGroup.set(resolvedGroupIndex, list)
  }

  for (const [groupIndex, list] of indicesByGroup) {
    indicesByGroup.set(groupIndex, [...new Set(list)].sort((a, b) => a - b))
  }
  return indicesByGroup
}

export function buildRouteDirectionAudioManifest(options = {}) {
  const audioRoot = options.audioRoot ?? ROUTE_AUDIO_PUBLIC_ROOT
  const routeIds = options.routeIds ?? discoverSyncedDirectionRouteIds({ audioRoot })

  /** @type {Record<string, Record<number, number[]>>} */
  const indicesByRoute = {}

  for (const routeId of routeIds) {
    const scanned = scanDirectionAudioIndices(audioRoot, routeId)
    if (scanned.size === 0) continue
    indicesByRoute[routeId] = Object.fromEntries(scanned.entries())
  }

  const routeBlocks = Object.entries(indicesByRoute)
    .map(([routeId, groups]) => {
      const groupLines = Object.entries(groups)
        .sort(([a], [b]) => Number(a) - Number(b))
        .map(([groupIndex, indices]) => `    ${groupIndex}: [${indices.join(', ')}] as const,`)
        .join('\n')
      return `  ${JSON.stringify(routeId)}: {\n${groupLines}\n  },`
    })
    .join('\n')

  const contents = `/** Generated by scripts/build-route-direction-audio-manifest.mjs — do not edit by hand. */

import { routes } from './routes'
import type { RouteStopAudioAtRow } from './routeStopAudio21A'
import { routeAudioFilePrefix } from '../utils/routeAudioFilePrefix'

export const ROUTE_DIRECTION_AUDIO_ROUTE_IDS = [${Object.keys(indicesByRoute)
    .map((id) => JSON.stringify(id))
    .join(', ')}] as const

const ROUTE_DIRECTION_AT_STOP_INDICES: Readonly<
  Record<string, Readonly<Record<number, readonly number[]>>>
> = {
${routeBlocks}
}

function audioUrlForAtStop(routeId: string, directionGroupIndex: number, atStopIndex: number): string {
  const group = routes.find((route) => route.id === routeId)?.stops?.[directionGroupIndex]
  const dirKey = (group?.directionKey ?? (directionGroupIndex === 0 ? 'N' : 'S')).toLowerCase()
  const prefix = routeAudioFilePrefix(routeId)
  return \`./audio/routes/\${routeId}/\${prefix}-\${dirKey}-at-\${String(atStopIndex).padStart(2, '0')}.mp3\`
}

export function getRouteDirectionStopAudioByAtIndex(
  routeId: string,
  directionGroupIndex: number,
): Map<number, RouteStopAudioAtRow> | null {
  const indices = ROUTE_DIRECTION_AT_STOP_INDICES[routeId]?.[directionGroupIndex]
  const stopList = routes.find((route) => route.id === routeId)?.stops?.[directionGroupIndex]?.list
  if (!indices?.length || !stopList?.length) return null

  const map = new Map<number, RouteStopAudioAtRow>()
  for (const at of indices) {
    const next = stopList[at + 1]
    const label = next?.name
    if (!label) continue
    map.set(at, {
      atStopIndex: at,
      nextStopLabel: label,
      audioUrl: audioUrlForAtStop(routeId, directionGroupIndex, at),
    })
  }
  return map.size > 0 ? map : null
}
`

  writeFileSync(manifestFile, contents, 'utf8')
  return {
    routes: Object.keys(indicesByRoute),
    clipCount: Object.values(indicesByRoute).reduce(
      (sum, groups) => sum + Object.values(groups).reduce((inner, list) => inner + list.length, 0),
      0,
    ),
  }
}

const isMain = process.argv[1]?.endsWith('build-route-direction-audio-manifest.mjs')
if (isMain) {
  const result = buildRouteDirectionAudioManifest()
  console.log(
    `[route-direction-audio] manifest: ${result.routes.length} route(s), ${result.clipCount} clip(s)`,
  )
}
