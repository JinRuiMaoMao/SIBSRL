import { existsSync, readdirSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { parseMp3StopHint } from './lib/stop-name-audio-match.mjs'

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
export const STOP_NAME_AUDIO_DIR = 'stop-names'
export const STOP_NAME_AUDIO_PUBLIC = resolve(root, 'public', 'audio', 'routes', STOP_NAME_AUDIO_DIR)
export const STOP_NAME_AUDIO_URL_PREFIX = `./audio/routes/${STOP_NAME_AUDIO_DIR}`
export const STOP_NAME_AUDIO_MANIFEST = resolve(root, 'src', 'data', 'stopNameAudioManifest.ts')

/** @param {string} filename */
export function encodeAudioFilename(filename) {
  return [...filename]
    .map((ch) => (/[A-Za-z0-9._-]/.test(ch) ? ch : encodeURIComponent(ch)))
    .join('')
}

export function buildStopNameAudioManifest(options = {}) {
  const sourceDir = options.sourceDir ?? STOP_NAME_AUDIO_PUBLIC
  /** @type {Array<{ file: string, hint: string, passSuffix: number | null, kind: string }>} */
  const entries = []

  if (existsSync(sourceDir)) {
    for (const file of readdirSync(sourceDir).filter((f) => f.toLowerCase().endsWith('.mp3'))) {
      const parsed = parseMp3StopHint(file)
      if (!parsed) continue
      entries.push({
        file,
        hint: parsed.hint,
        passSuffix: parsed.passSuffix,
        kind: parsed.kind,
      })
    }
  }

  entries.sort((a, b) => a.file.localeCompare(b.file, 'zh-Hans'))

  const entryLines = entries
    .map(
      (entry) =>
        `  { file: ${JSON.stringify(entry.file)}, hint: ${JSON.stringify(entry.hint)}, passSuffix: ${entry.passSuffix ?? 'null'}, kind: ${JSON.stringify(entry.kind)} },`,
    )
    .join('\n')

  const contents = `/** Generated by scripts/build-stop-name-audio-manifest.mjs — do not edit by hand. */

import type { BilingualText } from '../types/route'
import { STOP_NAME_ALIASES } from '../utils/stopNameAudioMatch'

export const STOP_NAME_AUDIO_URL_PREFIX = ${JSON.stringify(STOP_NAME_AUDIO_URL_PREFIX)}

export interface StopNameAudioManifestEntry {
  file: string
  hint: string
  passSuffix: number | null
  kind: 'stop' | 'alight'
}

export const STOP_NAME_AUDIO_MANIFEST: readonly StopNameAudioManifestEntry[] = [
${entryLines}
]

function stopNameMatchesHint(stopName: BilingualText, hint: string): boolean {
  const zh = stopName.zh?.trim() ?? ''
  const en = stopName.en?.trim() ?? ''
  const normalizedHint = hint.trim()
  if (!normalizedHint) return false

  if (zh && (zh === normalizedHint || zh.includes(normalizedHint) || normalizedHint.includes(zh))) {
    return true
  }

  if (en && (en === normalizedHint || en.includes(normalizedHint) || normalizedHint.includes(en))) {
    return true
  }

  const aliases = STOP_NAME_ALIASES[en] ?? []
  return aliases.some(
    (alias) =>
      alias === normalizedHint ||
      alias.includes(normalizedHint) ||
      normalizedHint.includes(alias),
  )
}

export function passIndexForNextStop(
  stopList: { name: BilingualText }[],
  nextIndex: number,
): number {
  const next = stopList[nextIndex]
  if (!next) return 0
  const nextZh = next.name.zh
  const nextEn = next.name.en
  let pass = 0
  for (let i = 0; i < nextIndex; i++) {
    const s = stopList[i]
    if (nextZh && s.name.zh === nextZh) pass++
    else if (nextEn && s.name.en === nextEn) pass++
  }
  return pass
}

function encodeAudioFilename(filename: string): string {
  return [...filename]
    .map((ch) => (/[A-Za-z0-9._-]/.test(ch) ? ch : encodeURIComponent(ch)))
    .join('')
}

export function findStopNameAudio(
  nextStop: BilingualText,
  passIndex: number,
): { audioUrl: string; sourceFile: string } | undefined {
  const candidates = STOP_NAME_AUDIO_MANIFEST.filter(
    (entry) => entry.kind === 'stop' && stopNameMatchesHint(nextStop, entry.hint),
  )
  if (candidates.length === 0) return undefined

  const desiredPass = passIndex + 1
  const exactPass = candidates.find((c) => c.passSuffix === desiredPass)
  const chosen =
    exactPass ??
    candidates.find((c) => c.passSuffix == null) ??
    (candidates.length === 1 ? candidates[0] : candidates.find((c) => c.passSuffix === 1)) ??
    candidates[0]

  if (!chosen) return undefined

  return {
    sourceFile: chosen.file,
    audioUrl: \`\${STOP_NAME_AUDIO_URL_PREFIX}/\${encodeAudioFilename(chosen.file)}\`,
  }
}
`

  writeFileSync(STOP_NAME_AUDIO_MANIFEST, contents, 'utf8')
  return { files: entries.length }
}

const isMain = process.argv[1]?.endsWith('build-stop-name-audio-manifest.mjs')
if (isMain) {
  const result = buildStopNameAudioManifest()
  console.log(`[stop-names] manifest: ${result.files} mp3(s)`)
}
