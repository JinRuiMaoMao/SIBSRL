import { cpSync, existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

/** 详情 route id → 世界地图走线 canonical id（与 route-maps 目录一致时可复用编号） */
export const WORLD_MAP_ROUTE_ALIASES = {
  '21A': '21',
}

const root = resolve(fileURLToPath(import.meta.url), '..', '..')
const sourceDir = resolve(root, 'data', 'world-map-routes')
const manifestFile = resolve(root, 'src', 'data', 'worldMapRoutesManifest.generated.ts')
const publicDir = resolve(root, 'public', 'world-map-routes')

function readRouteFile(filePath) {
  const raw = JSON.parse(readFileSync(filePath, 'utf8'))
  if (!raw.routeId || !Array.isArray(raw.directions)) {
    throw new Error(`Invalid world map route file: ${filePath}`)
  }
  for (const direction of raw.directions) {
    if (!Array.isArray(direction.points) || direction.points.length < 2) {
      throw new Error(`Route ${raw.routeId} direction ${direction.directionIndex} needs at least 2 points`)
    }
  }
  return raw
}

export function buildWorldMapRoutesManifest() {
  /** @type {Record<string, { directions: { directionIndex: number; points: number[][] }[] }>} */
  const byRouteId = {}

  if (existsSync(sourceDir)) {
    for (const entry of readdirSync(sourceDir, { withFileTypes: true })) {
      if (!entry.isFile() || !entry.name.endsWith('.json')) continue
      const data = readRouteFile(join(sourceDir, entry.name))
      byRouteId[data.routeId] = {
        directions: data.directions.map((direction) => ({
          directionIndex: direction.directionIndex ?? 0,
          points: direction.points,
        })),
      }
    }
  }

  const aliasEntries = Object.entries(WORLD_MAP_ROUTE_ALIASES)
    .map(([from, to]) => `  '${from}': '${to}',`)
    .join('\n')

  const routeEntries = Object.entries(byRouteId)
    .map(([id, data]) => `  '${id}': ${JSON.stringify(data, null, 2).replace(/\n/g, '\n  ')},`)
    .join('\n')

  const contents = `/** Generated by scripts/build-world-map-routes-manifest.mjs — do not edit by hand. */

export type WorldMapPoint = readonly [number, number]

export interface WorldMapRouteDirection {
  directionIndex: number
  points: readonly WorldMapPoint[]
}

export interface WorldMapRoutePaths {
  directions: readonly WorldMapRouteDirection[]
}

export const WORLD_MAP_ROUTE_ALIASES: Record<string, string> = {
${aliasEntries}
}

export const WORLD_MAP_ROUTE_PATHS: Record<string, WorldMapRoutePaths> = {
${routeEntries}
}
`

  writeFileSync(manifestFile, contents, 'utf8')

  if (existsSync(sourceDir)) {
    mkdirSync(publicDir, { recursive: true })
    cpSync(sourceDir, publicDir, { recursive: true })
  }

  console.log(`[world-map-routes] 已生成 ${Object.keys(byRouteId).length} 条走线 → ${manifestFile}`)
  return { count: Object.keys(byRouteId).length, manifestFile }
}

const isMain = process.argv[1]?.endsWith('build-world-map-routes-manifest.mjs')
if (isMain) {
  buildWorldMapRoutesManifest()
}
