/**
 * Scans route stop lists and writes src/data/stopAliases.generated.ts
 * Run: node scripts/generate-stop-aliases.mjs
 */
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import { createServer } from 'vite'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const outPath = join(root, 'src', 'data', 'stopAliases.generated.ts')

const server = await createServer({ configFile: join(root, 'vite.config.ts'), logLevel: 'error' })
try {
  const { routes } = await server.ssrLoadModule('/src/data/routes.ts')
  const { mergeRoutesByBaseNumber } = await server.ssrLoadModule('/src/utils/routeMerge.ts')
  const { canonicalStopKey } = await server.ssrLoadModule('/src/utils/stopIdentity.ts')

  const displayRoutes = mergeRoutesByBaseNumber(routes)
  /** @type {Map<string, { routeCount: number, variants: Map<string, number> }>} */
  const groups = new Map()

  for (const route of displayRoutes) {
    for (const group of route.stops ?? []) {
      for (const stop of group.list) {
        const canonical = canonicalStopKey(stop.name.zh, stop.name.en)
        const zh = stop.name.zh.trim()
        if (!zh) continue

        let entry = groups.get(canonical)
        if (!entry) {
          entry = { routeCount: 0, variants: new Map() }
          groups.set(canonical, entry)
        }
        entry.routeCount++
        entry.variants.set(zh, (entry.variants.get(zh) ?? 0) + 1)
      }
    }
  }

  /** @type {Record<string, string>} */
  const aliases = {}

  for (const [canonical, { variants }] of groups) {
    const ranked = [...variants.entries()].sort((a, b) => {
      if (b[1] !== a[1]) return b[1] - a[1]
      if (a[0].length !== b[0].length) return a[0].length - b[0].length
      return a[0].localeCompare(b[0], 'zh-Hans')
    })
    if (ranked.length <= 1) continue

    const representative = ranked[0][0]
    for (const [variant] of ranked.slice(1)) {
      if (variant === representative) continue
      if (aliases[variant] && aliases[variant] !== representative) {
        console.warn(
          `Alias conflict: ${variant} -> ${aliases[variant]} (skip -> ${representative})`,
        )
        continue
      }
      aliases[variant] = representative
    }

    if (!/[\u4e00-\u9fff]/.test(canonical)) continue
    if (representative !== canonical && !aliases[canonical]) {
      aliases[canonical] = representative
    }
  }

  const sortedEntries = Object.entries(aliases).sort((a, b) => a[0].localeCompare(b[0], 'zh-Hans'))
  const body = sortedEntries.map(([from, to]) => `  ${JSON.stringify(from)}: ${JSON.stringify(to)},`).join('\n')

  writeFileSync(
    outPath,
    `/** Auto-generated by scripts/generate-stop-aliases.mjs — do not edit */\nexport const GENERATED_ZH_STOP_ALIASES: Record<string, string> = {\n${body}\n}\n`,
    'utf8',
  )

  console.log(`Wrote ${sortedEntries.length} aliases -> ${outPath}`)
} finally {
  await server.close()
}
