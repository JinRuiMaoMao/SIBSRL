import { existsSync, readdirSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import {
  encodeNpcPathSegment,
  EXCLUDED_NPC_CATEGORIES,
  NPC_CATEGORY_ORDER,
  npcAudioRelativeUrl,
  sortNpcCategories,
} from './lib/npc-audio-path.mjs'
import { npcCategoryText, npcItemTitle } from './lib/npc-titles.mjs'

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
export const NPC_AUDIO_PUBLIC = resolve(root, 'public', 'audio', 'npc')
const manifestFile = resolve(root, 'src', 'data', 'npcAudio.ts')

function isAudioFile(name) {
  return /\.(mp3|ogg)$/i.test(name)
}

function titleFromFilename(category, filename) {
  return npcItemTitle(category, filename)
}

function slugifyCategory(category) {
  return encodeNpcPathSegment(category)
}

export function buildNpcManifest(options = {}) {
  const sourceDir = options.sourceDir ?? NPC_AUDIO_PUBLIC
  /** @type {string[]} */
  const categories = []
  /** @type {Array<{ id: string, category: string, number: number, titleZh: string, titleEn: string, audioUrl: string }>} */
  const items = []

  if (existsSync(sourceDir)) {
    for (const entry of readdirSync(sourceDir, { withFileTypes: true })) {
      if (!entry.isDirectory()) continue
      if (EXCLUDED_NPC_CATEGORIES.has(entry.name)) continue

      categories.push(entry.name)
      const files = readdirSync(join(sourceDir, entry.name))
        .filter(isAudioFile)
        .sort((a, b) => a.localeCompare(b, 'zh-Hans'))

      files.forEach((file, index) => {
        const title = titleFromFilename(entry.name, file)
        items.push({
          id: `npc-${slugifyCategory(entry.name)}-${index + 1}`,
          category: entry.name,
          number: index + 1,
          titleZh: title.zh,
          titleEn: title.en,
          audioUrl: npcAudioRelativeUrl(entry.name, file),
        })
      })
    }
  }

  const sortedCategories = sortNpcCategories(categories)

  items.sort((a, b) => {
    const ai = NPC_CATEGORY_ORDER.indexOf(a.category)
    const bi = NPC_CATEGORY_ORDER.indexOf(b.category)
    const ar = ai === -1 ? Number.MAX_SAFE_INTEGER : ai
    const br = bi === -1 ? Number.MAX_SAFE_INTEGER : bi
    if (ar !== br) return ar - br
    return a.number - b.number
  })

  const categoryLines = sortedCategories.map((c) => `  ${JSON.stringify(c)},`).join('\n')
  const itemLines = items
    .map((item) => {
      const categoryText = npcCategoryText(item.category)
      return `  { id: ${JSON.stringify(item.id)}, category: ${JSON.stringify(item.category)}, number: ${item.number}, title: { zh: ${JSON.stringify(item.titleZh)}, en: ${JSON.stringify(item.titleEn)} }, detail: { zh: ${JSON.stringify(categoryText.zh)}, en: ${JSON.stringify(categoryText.en)} }, audioUrl: ${JSON.stringify(item.audioUrl)} },`
    })
    .join('\n')

  const contents = `/** Generated by scripts/build-npc-manifest.mjs — do not edit by hand. */

import type { BilingualText } from '../types/route'

export type NpcAudioFilter = 'all' | string

export interface NpcAudioItem {
  id: string
  category: string
  number: number
  title: BilingualText
  detail: BilingualText
  audioUrl: string
}

export const NPC_CATEGORIES: readonly string[] = [
${categoryLines}
]

export const NPC_CATEGORY_TEXTS: Readonly<Record<string, BilingualText>> = {
${sortedCategories
  .map((category) => {
    const text = npcCategoryText(category)
    return `  ${JSON.stringify(category)}: { zh: ${JSON.stringify(text.zh)}, en: ${JSON.stringify(text.en)} },`
  })
  .join('\n')}
}

export const npcAudioItems: readonly NpcAudioItem[] = [
${itemLines}
]
`

  writeFileSync(manifestFile, contents, 'utf8')
  return { categories: sortedCategories.length, items: items.length }
}

const isMain = process.argv[1]?.endsWith('build-npc-manifest.mjs')
if (isMain) {
  const result = buildNpcManifest()
  console.log(`[npc] manifest: ${result.categories} categories, ${result.items} clip(s)`)
}
