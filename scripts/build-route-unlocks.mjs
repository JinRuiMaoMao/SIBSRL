/**
 * Build src/data/routeUnlocks.generated.ts from data/wiki-import/*.json
 */
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'
import { legacyWikiImportBasename, wikiImportPath } from './wiki-import-path.mjs'

const IMPORT_DIR = resolve('data/wiki-import')
const OVERRIDES_PATH = resolve('data/route-unlock-overrides.json')
const OUT = resolve('src/data/routeUnlocks.generated.ts')

/** Wiki 编号 → 游戏内编号（与 build-sibs-routes-ts 一致） */
const IN_GAME_ALIASES = {
  S1: 'S1A',
  S2: 'S2A',
}

function loadUnlock(id) {
  let path = wikiImportPath(IMPORT_DIR, id)
  if (!existsSync(path)) {
    path = resolve(IMPORT_DIR, `${legacyWikiImportBasename(id)}.json`)
  }
  if (!existsSync(path)) return null
  const route = JSON.parse(readFileSync(path, 'utf8'))
  const levelRequired = route.levelRequired ?? undefined
  const sunshardsRequired = route.sunshardsRequired ?? undefined
  if (levelRequired == null && sunshardsRequired == null) return null
  return { levelRequired, sunshardsRequired }
}

const unlocks = new Map()

function setUnlock(id, info) {
  const levelRequired = info.levelRequired ?? undefined
  const sunshardsRequired = info.sunshardsRequired ?? undefined
  if (levelRequired == null && sunshardsRequired == null) return
  unlocks.set(id, { levelRequired, sunshardsRequired })
}

for (const f of readdirSync(IMPORT_DIR)) {
  if (!f.endsWith('.json') || f.startsWith('_')) continue
  const route = JSON.parse(readFileSync(resolve(IMPORT_DIR, f), 'utf8'))
  setUnlock(route.id, route)
  if (route.variantUnlocks) {
    for (const [variantId, info] of Object.entries(route.variantUnlocks)) {
      setUnlock(variantId, info)
    }
  }
}

for (const [wikiId, gameId] of Object.entries(IN_GAME_ALIASES)) {
  const fromWiki = loadUnlock(wikiId)
  if (fromWiki) unlocks.set(gameId, fromWiki)
}

if (existsSync(OVERRIDES_PATH)) {
  const overrides = JSON.parse(readFileSync(OVERRIDES_PATH, 'utf8'))
  for (const [id, info] of Object.entries(overrides)) {
    setUnlock(id, { ...unlocks.get(id), ...info })
  }
}

const lines = [
  `/** Auto-generated by scripts/build-route-unlocks.mjs — do not edit by hand */`,
  `export interface RouteUnlockInfo {`,
  `  levelRequired?: number`,
  `  sunshardsRequired?: number`,
  `}`,
  ``,
  `export const ROUTE_UNLOCK_MAP: Record<string, RouteUnlockInfo> = {`,
]

for (const [id, info] of [...unlocks.entries()].sort(([a], [b]) => a.localeCompare(b))) {
  const parts = []
  if (info.levelRequired != null) parts.push(`levelRequired: ${info.levelRequired}`)
  if (info.sunshardsRequired != null) parts.push(`sunshardsRequired: ${info.sunshardsRequired}`)
  lines.push(`  '${id.replace(/'/g, "\\'")}': { ${parts.join(', ')} },`)
}

lines.push(`}`, ``)

writeFileSync(OUT, lines.join('\n'))
console.log(`Wrote ${unlocks.size} route unlock entries → ${OUT}`)
