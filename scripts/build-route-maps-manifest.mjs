import { existsSync, readdirSync, writeFileSync } from 'node:fs'
import { join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

/** 仅这些 canonical 线路 ID 可提供走向图静态 PNG（勿为其他线路添加资源）。 */
export const ROUTE_MAP_CANONICAL_IDS = ['77X', '21']

/** 详情/卡片 route id → 图片目录 canonical id */
export const ROUTE_MAP_ROUTE_ALIASES = {
  '77XA': '77X',
  '21A': '21',
}

const MAP_KINDS = ['path']
const IMAGE_EXTENSIONS = ['.png', '.webp', '.jpg', '.jpeg']

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
const sourceDir = resolve(root, 'public', 'route-maps')
const manifestFile = resolve(root, 'src', 'data', 'routeMapsManifest.ts')

function listKindsForRoute(routeDir) {
  const kinds = []
  for (const kind of MAP_KINDS) {
    if (IMAGE_EXTENSIONS.some((ext) => existsSync(join(routeDir, `${kind}${ext}`)))) {
      kinds.push(kind)
    }
  }
  return kinds
}

export function buildRouteMapsManifest(options = {}) {
  const allowed = new Set(options.allowedIds ?? ROUTE_MAP_CANONICAL_IDS)
  /** @type {Record<string, string[]>} */
  const byCanonicalId = {}

  if (existsSync(sourceDir)) {
    for (const entry of readdirSync(sourceDir, { withFileTypes: true })) {
      if (!entry.isDirectory() || entry.name.startsWith('.')) continue
      if (!allowed.has(entry.name)) continue
      const kinds = listKindsForRoute(join(sourceDir, entry.name))
      if (kinds.length > 0) byCanonicalId[entry.name] = kinds
    }
  }

  const aliasEntries = Object.entries(ROUTE_MAP_ROUTE_ALIASES)
    .map(([from, to]) => `  '${from}': '${to}',`)
    .join('\n')

  const manifestEntries = Object.entries(byCanonicalId)
    .map(([id, kinds]) => `  '${id}': [${kinds.map((k) => `'${k}'`).join(', ')}],`)
    .join('\n')

  const contents = `/** Generated by scripts/build-route-maps-manifest.mjs — do not edit by hand. */

export const ROUTE_MAP_CANONICAL_IDS = ${JSON.stringify(ROUTE_MAP_CANONICAL_IDS)} as const

export const ROUTE_MAP_ROUTE_ALIASES: Record<string, string> = {
${aliasEntries}
}

export type RouteMapViewKind = 'path' | 'height'

export const ROUTE_MAPS_BY_CANONICAL_ID: Record<string, readonly RouteMapViewKind[]> = {
${manifestEntries}
}

export function resolveRouteMapRouteId(routeId: string): string | null {
  const canonical = ROUTE_MAP_ROUTE_ALIASES[routeId] ?? routeId
  return Object.prototype.hasOwnProperty.call(ROUTE_MAPS_BY_CANONICAL_ID, canonical) ? canonical : null
}

export function listRouteMapKinds(routeId: string): RouteMapViewKind[] {
  const canonical = resolveRouteMapRouteId(routeId)
  if (!canonical) return []
  return [...(ROUTE_MAPS_BY_CANONICAL_ID[canonical] ?? [])]
}
`

  writeFileSync(manifestFile, contents, 'utf8')
  return { routes: Object.keys(byCanonicalId).length, kinds: Object.values(byCanonicalId).flat().length }
}

const isMain = process.argv[1]?.endsWith('build-route-maps-manifest.mjs')
if (isMain) {
  const result = buildRouteMapsManifest()
  console.log(`[route-maps] manifest: ${result.routes} route(s), ${result.kinds} image(s)`)
}
